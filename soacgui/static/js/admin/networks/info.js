$(function() {
    $("#deleteNetwork").on("click", function() {
        var network_id = $("#network_info").data("network_id");
        var network_name = $("#network_info").data("network_name");
        confirmDeleteNetwork(network_id, network_name);
    });
    $("#updateNetwork").on("click", function() {
        var network_id = $("#network_info").data("network_id");
        var network_name = $("#network_info").data("network_name");
        showUpdateNetworkModal(network_id);
    });
});